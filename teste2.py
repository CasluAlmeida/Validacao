# Lista de temperaturas
temperaturas = [23.6, 37.9, 25.1, 19.0, 29.8]

# a) Menor temperatura
menor_temperatura = min(temperaturas)

# b) Maior temperatura
maior_temperatura = max(temperaturas)

# c) Temperatura mÃ©dia
temperatura_media = sum(temperaturas) / len(temperaturas)

# Exibindo os resultados
print(f'Menor temperatura: {menor_temperatura}')
print(f'Maior temperatura: {maior_temperatura}')
print(f'Temperatura mÃ©dia: {temperatura_media:.2f}')
